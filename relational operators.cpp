#include<iostream>
using namespace std;

// relational operators are <,<=,>,>=,==,!=  ----->  this operators return true or false 
int main(){
     
    cout << (3<5) << endl;  // true->1
    cout << (3>5) << endl;  // false->0
    cout << (3<=5) << endl;  // true->1
    cout << (3>=5) << endl;  // false->0
    cout << (3==5) << endl;  // false->0
    cout << (3!=5) << endl;  // true->1
    return 0;
}